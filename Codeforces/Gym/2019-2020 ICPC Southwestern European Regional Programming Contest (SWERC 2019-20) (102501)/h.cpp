ll calc(ll x, ll y){

    if(x > y)
        return 0;

    ll bx = x / BLOCK_SIZE;
    ll by = y / BLOCK_SIZE;
    ll sum = 0;

    for(ll i = bx + 1; i < by; i++){
        sum += c3[i];
    }

    ll s = sarr[bx];

    for(ll i = bx * BLOCK_SIZE; i < x; i++){
        s = next(s);
    }

    if(bx != by){
        for(ll i = x; i < (bx + 1) * BLOCK_SIZE; i++){
            if(s % 2 == 0)
                sum++;
            s = next(s);
        }

        s = sarr[by];

        for(ll i = by * BLOCK_SIZE; i <= y; i++){
            if(s % 2 == 0)
                sum++;
            s = next(s);
        }
    } else {
        for(ll i = x; i <= y; i++){
            if(s % 2 == 0)
                sum++;
            s = next(s);
        }
    }

    return sum;
}

